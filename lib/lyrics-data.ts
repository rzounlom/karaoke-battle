export function getLyricsById(id: string): SongLyrics | undefined {
  return sampleLyrics.find((lyrics) => lyrics.id === id);
}

export function getCurrentLyricWord(
  lyrics: SongLyrics,
  currentTime: number
): LyricWord | null {
  return (
    lyrics.lyrics.find(
      (word) => currentTime >= word.startTime && currentTime <= word.endTime
    ) || null
  );
}

export function getUpcomingLyrics(
  lyrics: SongLyrics,
  currentTime: number,
  count: number = 5
): LyricWord[] {
  return lyrics.lyrics
    .filter((word) => word.startTime > currentTime)
    .slice(0, count);
}

export function getPreviousLyrics(
  lyrics: SongLyrics,
  currentTime: number,
  count: number = 3
): LyricWord[] {
  return lyrics.lyrics
    .filter((word) => word.endTime < currentTime)
    .slice(-count);
}
